/*
- the public class (name) should have same name with the file ie. in HelloWorld.java, the public class id HelloWorld.
- the public key word is an access modifier that allows a class or a method to be accessed by other classes.
- every java program must have the main class.
- static method allows methods to be called directly without creating objects to invoke them.
- public static void main initialises the body program and everything inside it should be computed.
- String[] args is the command line argument.
- System.out.println is a method that prints arguments to the console.

 */ 

public class HelloWorld{
    public static void main(String []args){
        System.out.println("Hello world");
    }
}